def raw(position, pieceType, pieceColor):
    advantage = 0
    endGame = False

    # How close is it to the center
    positionLst = list(position)
    position_horizontal = letterPosToIntPos(positionLst[0])-1
    position_vertical = int(positionLst[1])-1
    if pieceColor == 'black':
        position_horizontal = 7-position_horizontal
        position_vertical = 7-position_vertical

    diffCenter_horizontal = abs(4.5 - position_horizontal)
    diffCenter_vertical = abs(4.5 - position_vertical)

    advantage_center = 1/diffCenter_horizontal + 1/diffCenter_vertical
    advantage += advantage_center

    # Position BONUS

    if pieceType in POSITION_BONUS['eligibles']:

        if pieceType == 'BISHOP':
            
            # is it in the king side ?
            if position_horizontal < 4.5:
                advantage += 2

        if pieceType == 'KING':
            if endGame:
                advantage += POSITION_BONUS['KING_END'][position_vertical][position_horizontal]
            else:
                advantage += POSITION_BONUS['KING_MID'][position_vertical][position_horizontal]
        else:
            advantage += POSITION_BONUS[pieceType][position_vertical][position_horizontal]



    return advantage